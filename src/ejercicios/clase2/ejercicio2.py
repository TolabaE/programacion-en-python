# Implementar un programa en Python que pida al usuario que ingrese dos números y muestre la suma, resta, división y multiplicación de ambos.

num1 = int(input("ingrese el primer numero:  "))
num2 = int(input("ingrese el segundo numero:  "))

suma = num1 + num2
resta = num1 - num2
division = num1 / num2
multi = num1 * num2

print("la suma entre el numero 1 y el numero 2 es : ",suma)
print("la resta entre el numero 1 y el numero 2 es : ",resta)
print("la division entre el numero 1 y el numero 2 es : ",division)
print("la multiplicacion entre el numero 1 y el numero 2 es : ",multi)
